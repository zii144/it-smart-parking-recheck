// Demo QR codes wired to specific scenarios in backend/app/seed.py. These are
// still useful even with a real camera: (a) as a fallback when no camera is
// available/permitted, and (b) to jump straight to a specific judgement
// scenario without needing to print/display a QR code. Real, scannable PNGs
// for these same codes are generated by scripts/generate-demo-qr.mjs into
// public/demo-qr/ and can be viewed at /demo-qr.html for actual camera testing.
// qr-scanner's start() collapses EVERY getUserMedia failure (NotAllowedError,
// NotFoundError, NotReadableError, OverconstrainedError, …) into a single bare
// string throw of "Camera not found." - it tries several camera constraints in
// a loop with an empty `catch {}` and discards the real DOMException. So the
// thrown error tells us nothing about *why* it failed. Ask the Permissions API
// instead to tell "permission blocked" apart from "no usable camera".
// Returns "granted" | "denied" | "prompt" | null (null = can't determine;
// Firefox and older Safari don't support the "camera" permission name).
async function getCameraPermissionState() {
  try {
    if (!navigator.permissions?.query) return null;
    const status = await navigator.permissions.query({ name: "camera" });
    return status.state;
  } catch {
    return null;
  }
}

